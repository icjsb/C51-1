void LCDDisplay(uchar line,uchar row, uchar table[7][128])
{
   uchar i,j;
 for(i=0;i<8;i++)
 { 
      if(row<0x80)
      { 
        Left();
        }
   Write(line+i,CMD);
   Write(row,CMD);
   for(j=0;j<64;j++)
    {    
     Write(table[i][j],DATA);
     row+=1;
     }
   if(row>=0x80)
    {
      Right();
      row=row-64;
      }
    Write(line+i,CMD);
    Write(row,CMD);
    for(j=64;j<128;j++)
     {
       Write(table[i][j],DATA);
       row+=1;
       }
      if(row>=0x80)
        {
         row=row-0x40;
         }
    }
}		  
